CREATE TABLE IF NOT EXISTS CUSTOM_SELECT_AI_PROVIDERS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider VARCHAR2(100) UNIQUE,
    endpoint VARCHAR2(1000),
    auth VARCHAR2(1000)
);

CREATE TABLE IF NOT EXISTS CUSTOM_SELECT_AI_PROFILES (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_name VARCHAR2(256),
    attributes CLOB,
    provider VARCHAR2(256),
    model VARCHAR2(256),
    object_list CLOB,
    description VARCHAR2(1000)
);

CREATE TABLE IF NOT EXISTS CUSTOM_SELECT_AI_EMBEDDINGS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_name       VARCHAR2(256),
    request_id      VARCHAR2(256),
    question        VARCHAR2(4000),
    embedding       VECTOR(*, FLOAT32) NOT NULL,
    sql_text        VARCHAR2(4000),
    optime          TIMESTAMP(6) DEFAULT SYSTIMESTAMP
);
